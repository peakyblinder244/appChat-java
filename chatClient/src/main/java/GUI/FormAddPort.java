    private void themPortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themPortActionPerformed
        
        String stport = layPort.getText();
        File f = new File("fileConfig.txt");
        try {
            FileWriter fw = new FileWriter(f);
            
            for (String i : danhSachPort) {
                fw.write(i + "\n");
            }
            fw.write(stport);
            fw.close();
            JOptionPane.showMessageDialog(null,"Thêm Thành Công!");
            dispose();
            JFrame frame = new Login();
            frame.setResizable(false);
            frame.setTitle("Thêm Port");
            frame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            frame.setLocationRelativeTo(null);
            frame.setVisible(true);
            dispose();
        } catch (IOException ex) {
            System.out.println("Loi ghi file: " + ex);
        }
        
    }//GEN-LAST:event_themPortActionPerformed

