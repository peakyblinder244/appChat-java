    private void xoaPortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoaPortActionPerformed
        String port = layPort.getSelectedItem().toString();
        if (danhSachPort.remove(port)) {
            File f = new File("fileConfig.txt");
            try {
                FileWriter fw = new FileWriter(f);
                for (String i : danhSachPort) {
                    fw.write(i + "\n");
                }
                fw.close();
            } catch (IOException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Xóa Thành Công!");

            JFrame frame = new Login();
            frame.setResizable(false);
            frame.setTitle("Đăng Nhập");
            frame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            frame.setLocationRelativeTo(null);
            frame.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Xóa Không Thành Công! Mời Thử Lại!");
        }
    }//GEN-LAST:event_xoaPortActionPerformed

    public List<String> layDanhSachPort() {
        Scanner sc = null;
        List<String> list = new ArrayList<String>();
        try {
            File f = new File("fileConfig.txt");
            sc = new Scanner(f);
            while (sc.hasNextLine()) {
                String line = sc.nextLine();
                list.add(line);
            }
            sc.close();
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            if (sc != null) {
                sc.close();
            }
        }
        if (list.isEmpty()) {
            System.exit(0);
        }
        return list;
    }

