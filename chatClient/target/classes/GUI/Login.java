    private void dangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dangNhapActionPerformed
        String strNull = "";
        String username = userName.getText();
        String password = passWord.getText();
        if (username.equals(strNull) || password.equals(strNull)) {
            JOptionPane.showMessageDialog(null, "Tài Khoản Và Mật Khẩu Không Được Để Trống!");
            return;
        }
        try {
            if (LoginController.Login(username, password)) {
                InetAddress ip = InetAddress.getByName(layIp.getText());
                int port = Integer.valueOf(layPort.getSelectedItem().toString());

                JFrame frame = new FormClient(ip, port, username);
                frame.setResizable(false);
                frame.setTitle("Chat Client: " + username);
                frame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
                frame.setLocationRelativeTo(null);
                frame.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Đăng Nhập Thất Bại!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UnknownHostException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_dangNhapActionPerformed

